from random import choice
from unittest import TestCase


class RandomizedSet:

    def __init__(self):
        self.d, self.ref_list = {}, []

    def insert(self, val: int) -> bool:
        if val not in self.d:
            self.d[val] = len(self.ref_list)
            self.ref_list.append(val)
            return True
        return False

    def remove(self, val: int) -> bool:
        if val in self.d:
            position = self.d[val]
            last_value = self.ref_list[-1]
            self.ref_list[position] = last_value
            self.ref_list.pop() # Order of pop should be after insertion.Otherwise, it fails for single element ref_list

            self.d[last_value] = position


            del self.d[val]

            return True
        return False

    def getRandom(self) -> int:
        return choice(self.ref_list)


